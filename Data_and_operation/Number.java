package Data_and_operation;

public class Number {
    public static void main(String[] args) {
        //Operator
        System.out.println(6 + 2);
        System.out.println(6 - 2);
        System.out.println(6 * 2);
        System.out.println(6 / 2); //사칙기호들을 연산자 operator라고 부른다.

        System.out.println(Math.PI);
        System.out.println(Math.floor(Math.PI)); //floor는 바닥내림
        System.out.println(Math.ceil(Math.PI)); //ceil는 올림
    }
}
